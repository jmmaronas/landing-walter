function capitalize(string){
    const result=string.toLowerCase()
    .split(' ')
    .map(strWord => strWord.length>=3?strWord.substring(0, 1)
                    .toUpperCase() + strWord.substring(1, strWord.length):strWord)
    .join(' ');
    return result
}